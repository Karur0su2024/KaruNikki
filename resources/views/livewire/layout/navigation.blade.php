                            {{ auth()->user()->name }}
                        </a>
                        <ul class="dropdown-menu dropdown-menu-end" aria-labelledby="navbarDropdown">
                            <li><button wire:click='logout' class="dropdown-item">{{ __('Log Out') }}</button></li>
